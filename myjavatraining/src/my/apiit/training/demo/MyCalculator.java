package my.apiit.training.demo;

public class MyCalculator {

	// + add(i1:int, i2:int) : int
	public static int add( int i1, int i2 ) {
		return i1 + i2;
	}
	// + minus(i1:int, i2:int) : int
	public static int minus( int i1, int i2 ) {
		return i1 - i2;
	}
	// + mul(i1:int, i2:int) : int
	public static int mul( int i1, int i2 ) {
		return i1 * i2;
	}
	// + div(i1:int, i2:int) : int
	public static int div( int i1, int i2 ) {
		return i1 / i2;
	}
	// + mod(i1:int, i2:int) : int
	public static int mod( int i1, int i2 ) {
		return i1 % i2;
	}
}
