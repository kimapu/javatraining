package my.apiit.training.demo;

public class Calculator {

	//	+ i1 : int
	public int i1; //instance variable
	//	+ i2 : int
	public int i2; //instance variable
	
	
	//the following are the instance methods
	//	+ add() : int
	public int add() { return i1 + i2; } 
	
	//	+ minus() : int
	public int minus() { return i1 - i2; }
	
	//	+ mul() : int
	public int mul() { return i1 * i2; }
	
	//	+ div() : int
	public int div() { return i1 / i2; }
	
	//	+ mod() : int
	public int mod() { return i1 % i2; }
	
}
